int zb=InCord("Веедите координту Z для точки А:");

System.Console.WriteLine($"Расстояние между точками A({xa};{ya};{za}) и B({xb};{yb};{zb}) в 3D составляет : {Math.Round(PointsDistance3D(xa,ya,za,xb,yb,zb),2)}");