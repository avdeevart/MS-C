bool TriangleCheck(int[] sideArray) //проверка условий задачи, возращает истину, если a+b>c и ложь если не так
{
    if ((sideArray[0]+sideArray[1])>sideArray[2]&& (sideArray[1]+sideArray[2])>sideArray[0]&&(sideArray[0]+sideArray[2])>sideArray[1])
    {return true;} 
    else{return false;}
   
}
//-----------------------------------------------------------------------------------------------------------------------------------
string ValidTriangle(bool triangle)
