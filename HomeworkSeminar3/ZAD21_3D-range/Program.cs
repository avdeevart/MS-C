int InCord(string txt)   // объявление метода ввода данных для определения координаткоординат
{
    System.Console.Write(txt);
    return Convert.ToInt32(Console.ReadLine());
}
//_____________________________________________________________________________________________________________________________

double PointsDistance3D(int ax, int ay, int az, int bx, int by, int bz) //объявление метода, возращающего расстояние между точками
{
    double ResultDistance=Math.Sqrt(Math.Pow(bx-ax,2)+Math.Pow(by-ay,2)+Math.Pow(bz-az,2));
    return ResultDistance;
}
